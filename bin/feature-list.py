#!/usr/bin/env python3
"""
feature-list — generate / read / update .harness/feature_list.json.

JSON (not markdown) by deliberate choice (Anthropic harness lesson): models are
less likely to inappropriately rewrite JSON than markdown. This file is the
project's canonical feature ground truth — what features exist, which acceptance
criteria pass, which paired ERRs are open. Read at session start; written
incrementally as features ship.

Usage:
    python3 feature-list.py regenerate           # rebuild from spec/prd/*.md
    python3 feature-list.py show                 # print summary
    python3 feature-list.py mark <feature> <ac> pass|fail
"""

import json
import os
import re
import sys
from datetime import datetime, timezone


PRD_DIR = "spec/prd"
OUT_DIR = ".harness"
OUT_FILE = ".harness/feature_list.json"


def frontmatter(text):
    fm = {}
    m = re.match(r"^---\n(.*?)\n---", text, re.DOTALL)
    if not m:
        return fm
    for line in m.group(1).splitlines():
        if ":" in line and not line.startswith(" "):
            k, _, v = line.partition(":")
            fm[k.strip()] = v.strip()
    return fm


def acceptance_criteria(text):
    """Parse '- [ ] AC1: ...' / '- [x] AC2: ...' style lines from the PRD."""
    acs = []
    for m in re.finditer(r"^\s*-\s*\[([ x])\]\s*(AC\d+)[:\s]+(.+)$", text, re.MULTILINE):
        acs.append({
            "id": m.group(2),
            "description": m.group(3).strip(),
            "passes": (m.group(1) == "x"),
        })
    return acs


def regenerate():
    """Rebuild feature_list.json from PRDs. Preserves passes-state from prior run when ids match."""
    prior = {}
    if os.path.exists(OUT_FILE):
        try:
            prior = {f["id"]: f for f in json.load(open(OUT_FILE)).get("features", [])}
        except Exception:
            prior = {}

    features = []
    if os.path.isdir(PRD_DIR):
        for fn in sorted(os.listdir(PRD_DIR)):
            if not fn.endswith(".md"):
                continue
            text = open(os.path.join(PRD_DIR, fn)).read()
            fm = frontmatter(text)
            fid = fm.get("feature_name", fn[:-3])
            acs = acceptance_criteria(text)

            # Preserve passes-state from prior run for AC ids that already existed.
            if fid in prior:
                prior_acs = {a["id"]: a for a in prior[fid].get("acceptance_criteria", [])}
                for ac in acs:
                    if ac["id"] in prior_acs:
                        ac["passes"] = prior_acs[ac["id"]].get("passes", ac["passes"])

            features.append({
                "id": fid,
                "prd": f"{PRD_DIR}/{fn}",
                "state": fm.get("state", "PLANNED"),
                "tier": fm.get("tier", ""),
                "acceptance_criteria": acs,
                "ac_total": len(acs),
                "ac_passing": sum(1 for a in acs if a["passes"]),
                "human_approval": fm.get("human_approval", "pending"),
            })

    out = {
        "schema_version": 1,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "feature_count": len(features),
        "features": features,
        "note": "Generated by `harness features regenerate` from spec/prd/*.md. "
                "Do not edit by hand — edit the PRDs, then regenerate. "
                "passes-state for matching AC ids is preserved across regenerations.",
    }

    os.makedirs(OUT_DIR, exist_ok=True)
    with open(OUT_FILE, "w") as f:
        json.dump(out, f, indent=2)

    # Add .harness/ to gitignore if not already
    gi = ".gitignore"
    if os.path.exists(gi):
        existing = open(gi).read()
    else:
        existing = ""
    if ".harness/" not in existing:
        with open(gi, "a") as f:
            f.write("\n# Harness local state (not version-controlled)\n.harness/\n")

    print(f"feature-list: wrote {OUT_FILE} — {len(features)} features, "
          f"{sum(f['ac_total'] for f in features)} criteria, "
          f"{sum(f['ac_passing'] for f in features)} passing")


def show():
    if not os.path.exists(OUT_FILE):
        print("feature-list: no file yet. Run: python3 feature-list.py regenerate", file=sys.stderr)
        sys.exit(2)
    data = json.load(open(OUT_FILE))
    print(f"\nFeature list — {data['feature_count']} features (generated {data['generated_at']})\n")
    for f in data["features"]:
        bar = ""
        if f["ac_total"]:
            bar = f"  [{f['ac_passing']}/{f['ac_total']} criteria pass]"
        print(f"  • {f['id']:<32} {f['state']:<20} T{f['tier']}{bar}")
        for ac in f["acceptance_criteria"]:
            check = "✓" if ac["passes"] else "✗"
            print(f"        {check} {ac['id']}: {ac['description'][:80]}")
        if not f["acceptance_criteria"]:
            print(f"        (no acceptance criteria parsed from PRD)")
    print()


def mark(feature_id, ac_id, status):
    if not os.path.exists(OUT_FILE):
        print("feature-list: regenerate first.", file=sys.stderr)
        sys.exit(2)
    data = json.load(open(OUT_FILE))
    passes = (status.lower() in ("pass", "passing", "true", "1", "yes", "ok"))
    found = False
    for f in data["features"]:
        if f["id"] != feature_id:
            continue
        for ac in f["acceptance_criteria"]:
            if ac["id"] != ac_id:
                continue
            ac["passes"] = passes
            found = True
        f["ac_passing"] = sum(1 for a in f["acceptance_criteria"] if a["passes"])
    if not found:
        print(f"feature-list: no match for {feature_id} / {ac_id}", file=sys.stderr)
        sys.exit(2)
    data["generated_at"] = datetime.now(timezone.utc).isoformat()
    with open(OUT_FILE, "w") as f:
        json.dump(data, f, indent=2)
    print(f"feature-list: {feature_id}.{ac_id} -> {'pass' if passes else 'fail'}")


def main():
    if len(sys.argv) < 2:
        print(__doc__, file=sys.stderr)
        sys.exit(2)
    cmd = sys.argv[1]
    if cmd == "regenerate":
        regenerate()
    elif cmd == "show":
        show()
    elif cmd == "mark":
        if len(sys.argv) < 5:
            print("usage: feature-list.py mark <feature_id> <ac_id> pass|fail", file=sys.stderr)
            sys.exit(2)
        mark(sys.argv[2], sys.argv[3], sys.argv[4])
    else:
        print(f"feature-list: unknown command: {cmd}", file=sys.stderr)
        sys.exit(2)


if __name__ == "__main__":
    main()
